    <h3>Politique de mobilité et de développement</h3>
    <p>
        La politique de coopération et de développement international du Cégep de Trois-Rivières s’inscrit dans le cadre des
        grandes orientations du Plan de développement institutionnel 2002-2007 et en continuité avec nos engagements du
        Projet éducatif « Développer l’autonomie » tout en permettant de contribuer à l’internationalisation de l’éducation
        québécoise. L’internationalisation de l’éducation est un processus d’intégration des dimensions internationales et
        interculturelles dans l’enseignement, la recherche et les services éducatifs rendus par l’État, les établissements
        d’enseignement et les organismes et entreprises de ce secteur.
    </p>
    <p>
        Par ailleurs, en proposant l’intégration de la dimension internationale dans le plus grand nombre de programmes, en
        valorisant les activités de mobilité étudiante, en favorisant l’accueil d’étudiantes et d’étudiants étrangers et en
        poursuivant l’exportation de son expertise à l’étranger, le Cégep de Trois-Rivières « participe à l’élaboration et à
        la réalisation de programmes de coopération avec l’extérieur dans le domaine de l’enseignement collégial » tel que
        le stipule l’article 6.01 de la Loi des collèges.
    </p>
    <p>La politique de coopération et de développement international du Cégep de Trois-Rivières a pour but d’énoncer les
        intentions du collège, de préciser les champs d’activités à développer, de proposer des moyens à mettre en oeuvre
        pour atteindre les objectifs et de déterminer les rôles et responsabilités.
    </p>
